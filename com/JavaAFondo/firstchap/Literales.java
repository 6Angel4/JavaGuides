package com.JavaAFondo.firstchap;

public class Literales {
    public static void main(String[] args) {
        //No es lo mismo "" que ''
        for (int i=0;i<5;i++){
            System.out.print(i+"A"+",");
        }
        System.out.println();
        for (int i=0;i<5;i++){
            System.out.print(i+'A'+",");
        }
    }
}
